    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Anime anime = new Anime();
        Dados dado = new Dados();
        anime.setNome(txtFieldNome.getText());
        anime.setGenero(txtFieldGenero.getText());
       anime.setNota(Double.parseDouble(txtFieldNota.getText()));
       anime.setClassficacao(anime.classificar(anime.getNota()));
       dado.salvar(anime);
       
       txtFieldNome.setText("");
       txtFieldGenero.setText("");
       txtFieldNota.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

