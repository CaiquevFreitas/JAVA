    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed

        Dados dado = new Dados();
        String nome = JOptionPane.showInputDialog("Digite o nome do Anime: ");
        for(Anime a: dado.seachEdit(nome)){
           txtFieldNome.setText(a.getNome());
           txtFieldGenero.setText(a.getGenero());
           txtFieldNota.setText(Double.toString(a.getNota()));
        }  
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Anime anime = new Anime();
        Dados d = new Dados();
        anime.setNome(txtFieldNome.getText());
        anime.setGenero(txtFieldGenero.getText());
        anime.setNota(Double.parseDouble(txtFieldNota.getText()));
        anime.setClassficacao(anime.classificar(anime.getNota()));
        d.editar(anime);
    }//GEN-LAST:event_btnAtualizarActionPerformed

